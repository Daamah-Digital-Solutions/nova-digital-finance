/**
 * Initialize theme by setting CSS custom properties
 */
export function initializeTheme(): void {
  const root = document.documentElement;

  // Set color custom properties
  Object.entries(colors.primary).forEach(([key, value]) => {
    root.style.setProperty(`--color-primary-${key}`, value);
  });

  Object.entries(colors.secondary).forEach(([key, value]) => {
    root.style.setProperty(`--color-secondary-${key}`, value);
  });

  Object.entries(colors.neutral).forEach(([key, value]) => {
    root.style.setProperty(`--color-neutral-${key}`, value);
  });

  // Set spacing custom properties
  Object.entries(spacing).forEach(([key, value]) => {
    root.style.setProperty(`--spacing-${key}`, value);
  });

  // Set border radius custom properties
  Object.entries(borderRadius).forEach(([key, value]) => {
    root.style.setProperty(`--border-radius-${key}`, value);
  });

  // Set font family
  root.style.setProperty('--font-sans', typography.fontFamily.sans.join(', '));
  root.style.setProperty('--font-mono', typography.fontFamily.mono.join(', '));
  root.style.setProperty('--font-display', typography.fontFamily.display.join(', '));

  // Set animation durations
  Object.entries(animation.duration).forEach(([key, value]) => {
    root.style.setProperty(`--duration-${key}`, value);
  });

  // Set animation timing functions
  Object.entries(animation.timing).forEach(([key, value]) => {
    root.style.setProperty(`--timing-${key}`, value);
  });

  console.log('Nova Finance design tokens initialized');
}

